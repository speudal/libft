#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;
	int				i;

	tmp = (unsigned char *)s;
	i = 0;
	while (tmp && n--)
	{
		tmp[i] = 0;
		i++;
	}
}
